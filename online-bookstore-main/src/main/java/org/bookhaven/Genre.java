package org.bookhaven;

/**
 * The {@code Genre} enum represents different genres that can be assigned to books.
 * It includes predefined genres such as ROMANCE, ADVENTURE, THRILLER, COMEDY, MYSTERY,
 * FANTASY, BIOGRAPHY, HISTORY, COOKING, TRAVEL, and SCIENCE.
 * <p>
 * This enum is used to categorize books based on their genre.
 *
 */
public enum Genre {
    ROMANCE,
    ADVENTURE,
    THRILLER,
    COMEDY,
    MYSTERY,
    FANTASY,
    BIOGRAPHY,
    HISTORY,
    COOKING,
    TRAVEL,
    SCIENCE
}
